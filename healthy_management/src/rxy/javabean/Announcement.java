package rxy.javabean;

public class Announcement {
	private int Announce;
	private int GardenDoctorID;
	private String DoctorName;
	private String Contents;
	private String Time;
	public int getAnnounce() {
		return Announce;
	}
	public void setAnnounce(int announce) {
		Announce = announce;
	}
	public int getGardenDoctorID() {
		return GardenDoctorID;
	}
	public void setGardenDoctorID(int gardenDoctorID) {
		GardenDoctorID = gardenDoctorID;
	}
	public String getDoctorName() {
		return DoctorName;
	}
	public void setDoctorName(String doctorName) {
		DoctorName = doctorName;
	}
	public String getContents() {
		return Contents;
	}
	public void setContents(String contents) {
		Contents = contents;
	}
	public String getTime() {
		return Time;
	}
	public void setTime(String time) {
		Time = time;
	}
	
}
